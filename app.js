function sortWritingItems(items) {
  return [...items].sort((a, b) => {
    const timeA = getPublishedTime(a);
    const timeB = getPublishedTime(b);
    if (timeA !== timeB) {
      return timeB - timeA;
    }

    return String(a.title || "").localeCompare(String(b.title || ""));
  });
}

function getPublishedTime(item) {
  const publishedAt = item?.publishedAt || item?.pubDate || item?.date || "";
  const parsed = Date.parse(publishedAt);
  return Number.isFinite(parsed) ? parsed : 0;
}

function formatPublishedLabel(value, language = "en") {
  const parsed = Date.parse(value || "");
  if (!Number.isFinite(parsed)) {
    return "";
  }

  return new Intl.DateTimeFormat(language === "it" ? "it-IT" : "en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
  }).format(new Date(parsed));
}

function estimateReadTime(text, language = "en") {
  const words = String(text || "")
    .trim()
    .split(/\s+/)
    .filter(Boolean).length;
  const minutes = Math.max(1, Math.ceil(words / 180));
  return language === "it" ? `${minutes} min` : `${minutes} min read`;
}

function formatReadTimeLabel(item, language = "en") {
  const readingMinutes = Number(item?.readingMinutes);
  if (Number.isFinite(readingMinutes) && readingMinutes > 0) {
    return language === "it" ? `${Math.max(1, Math.ceil(readingMinutes))} min` : `${Math.max(1, Math.ceil(readingMinutes))} min read`;
  }

  const text = [item?.title, item?.summary].filter(Boolean).join(" ");
  return estimateReadTime(text, language);
}

