function setupSpotlightCarousel() {
  const rail = document.querySelector("[data-spotlight-rail]");
  if (!(rail instanceof HTMLElement)) {
    return;
  }

  if (window.matchMedia?.("(prefers-reduced-motion: reduce)")?.matches) {
    return;
  }

  if (window.__spotlightCarouselTimer) {
    window.clearInterval(window.__spotlightCarouselTimer);
  }

  const getStep = () => {
    const card = rail.querySelector(".spotlight-card");
    if (!(card instanceof HTMLElement)) {
      return 320;
    }

    const gap = 12;
    return card.getBoundingClientRect().width + gap;
  };

  const tick = () => {
    const maxScrollLeft = Math.max(0, rail.scrollWidth - rail.clientWidth - 8);
    if (maxScrollLeft <= 0) {
      return;
    }

    const nextLeft = rail.scrollLeft + getStep();
    if (nextLeft >= maxScrollLeft) {
      rail.scrollTo({ left: 0, behavior: "smooth" });
      return;
    }

    rail.scrollBy({ left: getStep(), behavior: "smooth" });
  };

  window.__spotlightCarouselTimer = window.setInterval(tick, 4500);
}

function setLink(node, href) {
  if (!node) {
    return;
  }

  node.href = safeHref(href);
  if (isInternalHref(href)) {
    node.removeAttribute("target");
    node.removeAttribute("rel");
    node.removeAttribute("aria-label");
    return;
  }

  if (href) {
    node.target = "_blank";
    node.rel = "noreferrer";
    node.removeAttribute("aria-label");
    return;
  }

  node.removeAttribute("target");
  node.removeAttribute("rel");
  node.setAttribute("aria-label", "Link da aggiungere");
}

function setLinks(nodes, href) {
  Array.from(nodes || []).forEach((node) => setLink(node, href));
}

function safeHref(href) {
  return href || "#follow";
}

function linkAttrs(href) {
  if (isInternalHref(href)) {
    return "";
  }

  return href ? 'target="_blank" rel="noreferrer"' : 'aria-label="Link da aggiungere"';
}

function isInternalHref(href) {
  return (
    typeof href === "string" &&
    (href.startsWith("#") || href.startsWith("/") || href.startsWith("./") || href.startsWith("../"))
  );
}

function safeSrc(src) {
  return src || "./assets/ougo.png";
}

function setupReveal() {
  const nodes = document.querySelectorAll(".reveal");
  if (!("IntersectionObserver" in window)) {
    nodes.forEach((node) => node.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.18 },
  );

  nodes.forEach((node) => observer.observe(node));
}

function setupWritingSwitcher() {
  if (writingSwitcherBound) {
    return;
  }

  writingSwitcherBound = true;

  if (els.articleList) {
    els.articleList.addEventListener("click", (event) => {
      const target = event.target;
      if (!(target instanceof Element)) {
        return;
      }

      const button = target.closest("[data-writing-tab]");
      if (!button || !(button instanceof HTMLElement)) {
        return;
      }

      if (!window.matchMedia("(max-width: 920px)").matches) {
        return;
      }

      const nextId = button.dataset.writingTab;
      if (!nextId || nextId === activeWritingSectionId) {
        return;
      }

      activeWritingSectionId = nextId;
      rememberLanguagePreference(nextId === "italian-writing" ? "it" : "en");
      syncWritingSwitcherState();

      const targetSection = document.querySelector(
        `.writing-section[data-writing-id="${activeWritingSectionId}"]`,
      );
      targetSection?.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  }

  window.addEventListener("resize", syncWritingSwitcherState);
  syncWritingSwitcherState();
}

function setupPostCardActions() {
  if (!els.articleList) {
    return;
  }

  els.articleList.addEventListener("click", async (event) => {
    const shareButton = event.target.closest("[data-share-url]");
    if (shareButton instanceof HTMLElement) {
      event.preventDefault();
      await handleShareButton(shareButton);
      return;
    }

    const likeButton = event.target.closest("[data-like-id]");
    if (likeButton instanceof HTMLElement) {
      event.preventDefault();
      toggleLikeButton(likeButton);
    }
  });
}

function getPreferredWritingSectionId() {
  const preferredLanguage = getBrowserLanguage();
  if (preferredLanguage === "it") {
    return "italian-writing";
  }

  return "global-writing";
}

function getBrowserLanguage() {
  const storedLanguage = loadPreferredLanguage();
  if (storedLanguage) {
    return storedLanguage;
  }

  const values = [];
  const localeValues = [];
  const timeZone = (() => {
    try {
      return Intl.DateTimeFormat().resolvedOptions().timeZone || "";
    } catch {
      return "";
    }
  })();

  if (typeof navigator !== "undefined") {
    if (Array.isArray(navigator.languages)) {
      values.push(...navigator.languages);
    }
    if (typeof navigator.language === "string") {
      values.push(navigator.language);
    }
    if (typeof navigator.userLanguage === "string") {
      values.push(navigator.userLanguage);
    }
  }

  const resolvedLocale = (() => {
    try {
      return Intl.DateTimeFormat().resolvedOptions().locale || "";
    } catch {
      return "";
    }
  })();

  const numberFormatLocale = (() => {
    try {
      return Intl.NumberFormat().resolvedOptions().locale || "";
    } catch {
      return "";
    }
  })();

  if (resolvedLocale) {
    localeValues.push(resolvedLocale);
  }

  if (numberFormatLocale) {
    localeValues.push(numberFormatLocale);
  }

  const hasItalianLocale = [...values, ...localeValues].some(isItalianLocaleValue);
  const hasItalianTimeZone = isItalianTimeZone(timeZone);

  if (hasItalianLocale || hasItalianTimeZone) {
    return "it";
  }

  return "en";
}

function isItalianLocaleValue(value) {
  const normalized = String(value || "")
    .toLowerCase()
    .replace(/_/g, "-");
  if (!normalized) {
    return false;
  }

  return (
    normalized === "it" ||
    normalized.startsWith("it-") ||
    normalized.includes("-it-") ||
    normalized.endsWith("-it") ||
    normalized.includes("it-it")
  );
}

function isItalianTimeZone(value) {
  const normalized = String(value || "").toLowerCase();
  return normalized === "europe/rome" || normalized === "europe/san_marino" || normalized === "europe/vatican";
}

